    public function store(Request $request, $id)
    {
        $this->validate(request(), [
            'name' => 'required',
            'email' => 'required',
            'body' => 'required'
        ]);

        $comment = Comment::create([
            'name' => $request->name,
            'email' => $request->email,
            'body' => $request->body,
            'post_id' => $id
        ]);

        $post = Post::findOrFail($id);

        $post->comments()->save($comment);

        return redirect()->route('posts.show', ['id' => $id]);
    }

